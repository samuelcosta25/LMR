let dia;

dia = prompt('Informe um n°',"1-7");

if (dia==1){document.write("Domingo");}
else if (dia==2){document.write("Segunda");}

else if (dia==3){document.write("Terça");}

else if (dia==4){document.write("Quarta");}

else if (dia==5){document.write("Quinta");}

else  if (dia==6){document.write("Sexta");}

else if (dia==7){document.write("Sábado");}

else {document.write("Digite um valor valido");};